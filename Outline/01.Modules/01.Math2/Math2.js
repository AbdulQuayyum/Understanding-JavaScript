let add = function (n1, n2) {
    return n1 + n2;
}

let divide = function (n1, n2) {
    return n1 / n2
}

let subtract = function (n1, n2) {
    return n1 - n2
}

let multiply = function (n1, n2) {
    return n1 * n2
}

export let SpeedOfLight = "299792458 m/s"

export {
    add,
    divide,
    subtract,
    multiply,
    SpeedOfLight
}
